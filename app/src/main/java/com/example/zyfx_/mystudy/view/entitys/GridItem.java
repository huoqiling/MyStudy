public class GridItem {

    private int iconRes;
    private String iconName;

    public GridItem(int iconRes, String iconName) {
        this.iconRes = iconRes;
        this.iconName = iconName;
    }

    public int getIconRes() {
        return iconRes;
    }

    public String getIconName() {
        return iconName;
    }
}
